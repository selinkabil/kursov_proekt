package bg.tu_varna.sit.a2.f22621625.enums;

/**
 * Enum representing the status of a ticket.
 */
public enum TicketStatus {
    /**
     * Indicates that the ticket has been paid for.
     */
    PAID,

    /**
     * Indicates that the ticket is available for purchase.
     */
    AVAILABLE,

    /**
     * Indicates that the ticket has been booked but not yet paid for.
     */
    BOOKED
}
